robot.forward(step)
robot.end_fill()
robot.penup()
robot.setheading(EAST)
robot.forward((step+step))
robot.setheading(NORTH)
robot.pendown()
r = randint(0,255)
g = randint(0,255)
b = randint(0,255)
robot.fillcolor((r,g,b))
robot.begin_fill()
robot.forward(n3)
